'''
# Slope

# Slope : Measure steepness

# Formula :

Slope =
        Change in Y
        -----------
        Change in X
        
Example:
Point A = (1,2)
Point B = (3,6)


Calculation:
            (6 - 2)
            ---------
            (3 - 1)

            4 / 2

            2
'''

x1 = 1
y1 = 2

x2 = 3
y2 = 6

slope = (y2 - y1) / (x2 - x1)

print(slope)




