# COMPARISON OPERATORS

# Equal 
print(10 == 10) # True

# Not Equal
print(10 != 5) # True

# Greater Than 
print(20 > 10) # True

# Less Than
print(5 < 10) # True

# Greater Than Equal
print(20 >= 20) # True

# Less Than Equal
print(10 <= 20) # True

# CHEAT SHEET
# ==   Equal

# !=   Not Equal

# >    Greater Than

# <    Less Than

# >=   Greater Equal

# <=   Less Equal